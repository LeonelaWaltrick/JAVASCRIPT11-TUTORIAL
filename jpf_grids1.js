/* drawPuzzle() function used by the tutorial */
function drawPuzzle(hint, rating, puzzle) {

   var htmlString = "";

   var totalRows = puzzle.length;
   var totalCols = puzzle[0].length;

   var rowCount = [];
   var spaceCount;

   for (var i = 0; i < totalRows; i++) {
      rowCount[i] = "";
      spaceCount = 0;

      for (var j = 0; j < totalCols; j++) {
         if (puzzle[i][j] === "#") {
            spaceCount++;
            if (j === totalCols - 1) {
               rowCount[i] += spaceCount + "&nbsp;&nbsp;";
            }
         } else {
            if (spaceCount > 0) {
               rowCount[i] += spaceCount + "&nbsp;&nbsp;";
               spaceCount = 0;
            }
         }
      }
   }

   var colCount = [];
   for (var j = 0; j < totalCols; j++) {
      colCount[j] = "";
      spaceCount = 0;

      for (var i = 0; i < totalRows; i++) {
         if (puzzle[i][j] === "#") {
            spaceCount++;
            if (i === totalRows - 1) {
               colCount[j] += spaceCount + "<br />";
            }
         } else {
            if (spaceCount > 0) {
               colCount[j] += spaceCount + "<br />";
               spaceCount = 0;
            }
         }
      }
   }

   htmlString = "<table id='hanjieGrid'>";
   htmlString += "<caption>" + hint + " (" + rating + ")</caption>";
   htmlString += "<tr><th></th>";

   for (var j = 0; j < totalCols; j++) {
      htmlString += "<th class='cols'>" + colCount[j] + "</th>";
   }
   htmlString += "</tr>";

   for (var i = 0; i < totalRows; i++) {
      htmlString += "<tr><th class='rows'>&nbsp;" + rowCount[i] + "</th>";

      for (var j = 0; j < totalCols; j++) {
         if (puzzle[i][j] === "#") {
            htmlString += "<td class='filled'></td>";
         } else {
            htmlString += "<td class='empty'></td>";
         }
      }

      htmlString += "</tr>";
   }

   htmlString += "</table>";

   return htmlString;
}
